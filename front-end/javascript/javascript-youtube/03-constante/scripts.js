/*
    Variável
    - Utilize quando necessitar redefinir seu valor
*/
let valorIngressoAdulto = 20;
valorIngressoAdulto = 30;
console.log(valorIngressoAdulto);

/* 
    Constante
    - Não é possível redefinir o seu valor
    - É uma boa prática utilizá-lo para valores que não precisam ser redefinidos
*/

const valorIngressoCrianca = 20;
console.log(valorIngressoCrianca);