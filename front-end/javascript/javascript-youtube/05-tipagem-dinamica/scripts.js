/*
    Tipagem Dinamica
    - Java e C# são linguagens que devemos primeiro definir o tipo de dado da variável (tipagem não dinâmica)
    - Já o JavaScript descobre o tipo de variável em tempo de execução (tipagem dinâmica)
*/
 
let nome = "Tadeu";
console.log(typeof nome);    //Exibirá o tipo da variável (String)

let idade = 32;
console.log(typeof idade);   //Exibirá o tipo da variável (Number)

let estaAprovado = true;
console.log(typeof estaAprovado);   //Exibirá o tipo da variável (Boolean)

let sobreNome = undefined;
console.log(typeof sobreNome);  //Exibirá o tipo da variável (Undefined)

let corSelecionada = null;  
console.log(typeof corSelecionada)  //Exibirá o tipo da variável (Object)