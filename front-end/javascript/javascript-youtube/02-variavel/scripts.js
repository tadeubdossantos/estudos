//Variavel - armazena valores para serem usados posteriormente

/*
    Declaração de variável
    - let: tipo de variável da versão mais nova do linguagem
    - var: tipo de variável da versão anterior da linguagem
    - idade: nome da variável
    - =: operação de atribuição
    - 5: valor em si que está sendo atribuido à variável
    - ;: representa o final do comando
*/
let idade = 5;

//Impressão do valor da variável no console do navegador
console.log(idade);

//Outro exemplo de declaração de variavel, atribuição de valor na variável e exibição do valor da variável
let altura = 180;
console.log(altura);

/*
    Maneiras de nomear variáveis
    - Nunca deve começar com número; exemplo: 1altura
    - Nunca deve colocar espaços; exemplo: al tura
    
    Recomendado usar camelCase
    - camelCase: início 1º palavra em mínusculo e da palavra seguinte inciando em minúsculo; exemplo: nomeCompleto

    Observação: caracteres no javascript são case sensitive, portanto caracteres mínusculos e maísculos fazem diferença na declação das variáveis; exemplo: variavel "nome" é diferente da variável "Nome"
 */ 

